public class IndexBean {
    private String name;
    private String dir;
    private String path;
    private String tourxml;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getDir() {
        return dir;
    }

    public void setDir(String dir) {
        this.dir = dir;
    }

    public String getPath() {
        return path;
    }

    public void setPath(String path) {
        this.path = path;
    }

    public String getTourxml() {
        return tourxml;
    }

    public void setTourxml(String tourxml) {
        this.tourxml = tourxml;
    }
}
